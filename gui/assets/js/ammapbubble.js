			type: "circle",
			width: size,
			height: size,
			color: dataItem.color,
			longitude: latlong[id].longitude,
			latitude: latlong[id].latitude,
			title: dataItem.name + ": " + dataItem.avg,
			value: value,
			gval:dataItem["avg"]
